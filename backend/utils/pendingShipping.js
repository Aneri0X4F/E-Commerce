const pool = require(pool);

exports.pendingShipping = () => {
    pool.getConnection((err) => {
        if(err){
            res.send({
                data: null,
                msg: err.message,
                status: 0
            })
        }
        else{
            let fetch = 'SELECT * FROM shipping WHERE shippingStatus = "pending";';
            pool.query(fetch, (err,result) => {
                if(err){
                    res.send({
                        data: null,
                        msg: err.message,
                        status: 0
                    })
                }
                else{
                    res.send({
                        status: 1,
                        msg: 'Pending Shipping List',
                        data: result,
                    });
                }
            })
        }
    })
}
